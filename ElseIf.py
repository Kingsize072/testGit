grade = float(input("Check Your Grade : "))
if grade >= 80:
    print("A")
elif grade >= 75:
    print("B+")
elif grade >= 70:
    print("B")
elif grade >= 65:
    print("C+")
elif grade >= 60:
    print("C")
elif grade >= 55:
    print("D+")
elif grade >= 50:
    print("D")
else:
    print("F")
print("Good Bye!!!")
